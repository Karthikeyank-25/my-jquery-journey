// Selecting Elements :
//              Selecting the elements in the document by using jQuery library.

// Syntax :         (Same for selecting single and multiple elements)

// Selecting one element :
//              Instead of using "document.querSelector('h1')" to select one element.
document.querySelector = $("h1");

// Selecting all element :
//              Instead of using "document.querSelectorAll('h1')" to select one element.
document.querySelectorAll = $("h1");